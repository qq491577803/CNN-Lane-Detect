import re
'''
[1-9]\d*　     正整数
-[1-9]\d* 　 负整数
-?[1-9]\d*　整数
[1-9]\d*|0　 非负整数
-[1-9]\d*|0　　 非正整数
[1-9]\d*\.\d*|0\.\d*[1-9]\d*$　　 正浮点数
-([1-9]\d*\.\d*|0\.\d*[1-9]\d*)$　 负浮点数
-?([1-9]\d*\.\d*|0\.\d*[1-9]\d*|0?\.0+|0)$　 浮点数

'''
re_ = re.compile('-?[0-9]\d*')
data = 'AB-1.1-2CD--2EF---32-32500-90'
res1 = re_.findall(data)
print(res1) #res = ['-1', '1', '-2', '-2', '-32', '-32500', '-90']'

